


-- ------------ Write CREATE-USER-stage scripts -----------

CREATE USER AWS_DMS
    IDENTIFIED BY user_password
    DEFAULT TABLESPACE KUALI_DATA
    TEMPORARY TABLESPACE TEMP
    PROFILE DEFAULT
/




 




